import re


def clean_text(text):
    print(f"\n[Original]: {text[:100]}...")

    # 1. Braille Block (Standard)
    text = re.sub(r"[\u2800-\u28FF]+", "", text)

    # 2. Excessive Punctuation/Symbols
    text = re.sub(r"[#*^\\/\\|\\-_+]{3,}", "", text)

    # 3. Block Drawing / Geometric Shapes / Misc Symbols
    # U+2500-257F (Box Drawing), U+2580-259F (Block Elements)
    # U+2600-26FF (Misc Symbols like ♥)
    text = re.sub(r"[\u2500-\u26FF]+", "", text)

    print(f"[Cleaned]:  {text[:100]}...")
    return text


